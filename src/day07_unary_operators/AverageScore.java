package day07_unary_operators;

public class AverageScore {
    public static void main(String[] args) {
        int score1=90;
        int score2=80;
        int score3=100;
        int score4=95;
        double averageScore=(double)(score1+score2+score3+score4)/4;
        System.out.println("Average: "+averageScore);
    }
}
