package day06_arithmetic_operators;

public class CarInfo {
    public static void main(String[] args) {
        String model="Highlander";
        String driverName="Hilal";
        String licenseNumber="214HT35";
        int speed=118;
        boolean isAutomatic=true;
        char licenseClass='D';

        System.out.println("model = " + model);
        System.out.println("driverName = " + driverName);
        System.out.println("licenseNumber = " + licenseNumber);
        System.out.println("speed = " + speed);
        System.out.println("isAutomatic = " + isAutomatic);
        System.out.println("licenseClass = " + licenseClass);

    }
}
