package day08_relational_operators;

public class KgToPounds {
    public static void main(String[] args) {
        double kg = 30;
        double poundInKg = 2.20462;
        double kgToPound = kg*poundInKg;
        System.out.println(kg+ "kg is equal to "+kgToPound +" pounds");

        //Another Approach

        double kg1=30;
        kg1*=2.20462;
        System.out.println(kg1);




    }

}
