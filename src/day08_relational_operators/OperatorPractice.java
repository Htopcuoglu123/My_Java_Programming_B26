package day08_relational_operators;

public class OperatorPractice {
    public static void main(String[] args) {
        int b=10;//10
        b++;//11
        System.out.println(b);//11
        b--;//10
        System.out.println(b);//10
        System.out.println(b++);//10
        System.out.println(b++);//11
    }
}
