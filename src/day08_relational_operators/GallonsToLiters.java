package day08_relational_operators;

public class GallonsToLiters {
    public static void main(String[] args) {
        double gallon= 10;
        double literInGallon= 3.7854;
        double gallonToLiter=gallon*literInGallon;
        System.out.println(""+gallon+" is equal to "+gallonToLiter+" liters");

    }
}
