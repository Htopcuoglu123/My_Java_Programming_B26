package day04_variables;

public class PrimitiveDataTypes {
    public static void main(String[] args) {
        byte age = 60;
        System.out.println(age);

//        byte age2=200; 200 is too big for that variable type




        short year; //declared a "short typed" variable and named the variable as "year"
        year=2022; //assigned the number 2022 to the variable year
        year=2023;// reassigning a value to be 2023
        System.out.println(year);

//        long bigNumber=123445345346365663; this value is an int type but it is too big for int so we can use a long type variable but we need to use an L at the end

        long bigNumber=123244234344L;
        System.out.println(bigNumber);



    }
}
